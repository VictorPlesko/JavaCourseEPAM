package com.plesko.lesson1.entity;

public class Point {

    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        Point point = (Point) o;
        return x == point.x && y == point.y;
    }

    @Override
    public int hashCode() {
        int prime = 31;
        return prime * x + y;
    }

    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder("Point: ");
        sb.append("x = ").append(x);
        sb.append("; y = ").append(y);
        return sb.toString();
    }
}
