package com.plesko.lesson1.console;

public class GeometricConsole {

    public void printSquareArea(double area) {
        System.out.printf("The square area is %f", area);
    }

    public void printSquareDifference(double difference) {
        System.out.printf("Area difference is %f", difference);
    }

    public void printCircumference(double circumference) {
        System.out.printf("Circumference is %f", circumference);
    }

    public void printCircleArea(double area) {
        System.out.printf("The area of the circle is %f", area);
    }
}
